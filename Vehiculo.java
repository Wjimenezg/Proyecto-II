package matrizortogonal;

public class Vehiculo {
    // almacenamiento de los datos del vehiculo
    String placa;
    String color;
    String linea;
    String modelo;
    String propietario;

    public Vehiculo(String placa, String color, String linea, String modelo, String propietario) {
        // asignacion de los datos al objeto vehiculo
        this.placa = placa;
        this.color = color;
        this.linea = linea;
        this.modelo = modelo;
        this.propietario = propietario;
    } 
}
